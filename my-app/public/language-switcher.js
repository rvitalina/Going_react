function changeLanguage() {
  var languageSelector = document.getElementById("language-switcher");
  var selectedLanguage = languageSelector.value;
  var i18nElements = document.querySelectorAll("[data-i18]");

  i18nElements.forEach(function (element) {
    var translationKey = element.getAttribute("data-i18");
    var translation = getTranslation(selectedLanguage, translationKey);
    element.textContent = translation;
  });

  if(selectedLanguage === "ru") {
    document.querySelector(".booked-going-gone").style.top = "2010px";
  }

  localStorage.setItem("selectedLanguage", selectedLanguage);
}

function getTranslation(language, key) {
  if (language in i18Obj && key in i18Obj[language]) {
    return i18Obj[language][key];
  }

  return "";
}

//обработка перезагрузки страницы
window.onload = function () {
  var selectedLanguage = localStorage.getItem("selectedLanguage");
  if (selectedLanguage) {
    var languageSelector = document.getElementById("language-switcher");
    languageSelector.value = selectedLanguage;
    changeLanguage();
  }
};
